<template>
  <div>
    <List :columns="columns" :data="items">
      <div class="list-header">
        <div class="list-operations">
          <Button class="margin-right-sm" type="primary" @click="$router.push(`/klass/edit`)">新增</Button>
        </div>
        <div class="list-search">
          <Form inline >
            <Form-item prop="name">
              <i-input 
                placeholder="请输入客户名称" 
                v-model="input" 
                clearable>
              </i-input>
            </Form-item>
            <Form-item>
              <i-button type="primary" shape="circle" icon="ios-search"></i-button>
            </Form-item>
          </Form>
        </div>
      </div>
    </List>
  </div>
</template>
<script>
import List from '@/components/List.vue'
import AdminService from '@/api/admininfo'

export default {
  components: {
    List
  },
  data () {
    return {
      input:'',
      items:[],
      columns:[
      {
          title: '客户ID',
          key: 'id',
          width: 100,
          fixed: 'left'

      },
      {
          title: '客户姓名',
          key: 'name',
          width: 150,
      },
      {
          title: '电子邮箱',
          key: 'email',
          width: 200,
      },
      {
        title: '电话号码',
        key: 'phone',
        width: 150,
      },
      {
          title: '地址',
          key: 'adress',
          width: 200,
      },
      {
          title: '邮编',
          key: 'postcode',
          width: 100,
      },
      {
          title: '银行账户',
          key: 'bank-account-number',
          width: 200,
      },
      {
          title: '操作',
          key: 'action',
          fixed: 'right',
          render: (h, params) => h('ButtonGroup', [
              h('Button', {
              on: {
                  click: () => {
                  this.onDeleteCustomer(params.row.id)
                  }
              }
              }, '删除'),
          ])
      }
      ]
    }
  },
  created () {
    this.getcustomerList()
  },
  methods: {
    getcustomerList(){
      AdminService.list().then(res=>{
        this.items = res.data
      })
    },
    onDeleteCustomer(id){
      AdminService.deleteCustomer(id).then(res =>{
          console.log(res)
          this.$Message.success("删除成功")
          this.getcustomerList()
      })
    }
  }

}
</script>

